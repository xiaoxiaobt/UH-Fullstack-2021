const typeDefs = gql`
  type Author {
    name: String!
    born: Int
    id: ID!
    bookCount: Int!
  }

  type Book {
    title: String!
    published: Int!
    author: Author!
    genres: [String!]!
    id: ID!
  }

  type Query {
    bookCount: Int!
    authorCount: Int!
    allBooks(author: String, genre: String): [Book!]!
    allAuthors: [Author!]!
  }

  type Mutation {
    addBook(
      title: String!
      published: Int!
      author: String!
      genres: [String!]! 
    ): Book
    editAuthor(
      name: String!
      setBornTo: Int!
    ): Author
  }
`

const resolvers = {
  Query: {
    bookCount: () => Book.collection.countDocuments(),
    authorCount: () => Author.collection.countDocuments(),
    allBooks: async (root, args) => {
      if (args?.author && args?.genre) {
        const author = await Author.findOne({ name: args.author })
        return Book.find({ genres: { $in: [args.genre] }, author: author }).populate('author')
      } else if (args?.author) {
        const author = await Author.findOne({ name: args.author })
        return Book.find({ author: author }).populate('author')
      } else if (args?.genre) {
        return Book.find({ genres: { $in: [args.genre] } })
      } else {
        return Book.find({})
      }
    },
    allAuthors: () => Author.find({}),

  },
  Author: {
    bookCount: async (root) => {
      const author = await Author.findOne({ name: root.name })
      return Book.find({ author: author }).countDocuments()
    }
  },
  Mutation: {
    addBook: async (root, args) => {
      if ((await Author.findOne({ name: args.author })) === null) {
        const author = new Author({ name: args.author, born: null })
        try {
          await author.save()
        } catch (error) {
          throw new UserInputError(error.message, {
            invalidArgs: args,
          })
        }
      }

      const book = new Book({ ...args, author: await Author.findOne({ name: args.author }) })
      try {
        await book.save()
      } catch (error) {
        throw new UserInputError(error.message, {
          invalidArgs: args,
        })
      }
      return book
    },
    editAuthor: async (root, args) => {
      if ((await Author.findOne({ name: args.name })) === null) {
        return null
      } else {
        const author = await Author.findOne({ name: args.name })
        author.born = args.setBornTo
        try {
          await author.save()
        } catch (error) {
          throw new UserInputError(error.message, {
            invalidArgs: args,
          })
        }
        return author
      }
    }
  }
}

